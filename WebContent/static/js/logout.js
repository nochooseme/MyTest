function logout(){
	$.ajax({
		async:false,
		type:"post",
		url:"logout",
		data:{},
		contentType:"application/json",
		success:function(data){
			window.location.href=data.result;		
		},
		error:function(data){
			alert("发生错误！");
		}
	})
}
